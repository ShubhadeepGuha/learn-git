class pattern9
{
    public static void main(String args[])
    {
        for(int y=5;y>0;y--)
        {
         
            
            for(int x=5;x>0;x--)
            {
              
                if(y>x)
                {
                    System.out.print(y);
                }
                else{
                    System.out.print(x);
                }
           
            }
            System.out.println();
            
        }
    }
}